export function getCenterByCityName(name) {
    name = name.replace('市', '').replace('省', '');
    let pos = null;
    for (let i = 0; i < geo.length; i++) {
        const province = geo[i];
        if (name === province.ProvinceName) {
            pos = province.LatLng;
            break;
        }
        if (province.City) {
            for (let j = 0; j < province.City.length; j++) {
                const city = province.City[j];
                if (name === city.Name) {
                    pos = city.LatLng;
                    break;
                }
            }
        }
    }
    if (!pos) {
        for (let i = 0; i < geo.length; i++) {
            const province = geo[i];
            if (name.includes(province.ProvinceName)) {
                pos = province.LatLng;
                break;
            }
            if (province.City) {
                for (let j = 0; j < province.City.length; j++) {
                    const city = province.City[j];
                    if (name.includes(city.Name)) {
                        pos = city.LatLng;
                        break;
                    }
                }
            }
        }
    }

    return pos;
}